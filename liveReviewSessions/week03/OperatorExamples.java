package week03;

public class OperatorExamples {
    public static void main(String[] args) {

        int x = 10;
        x+= 2;
        System.out.println("x = " + x);
        
        x-=2;
        System.out.println("x = " + x);
        
        x*=2;
        System.out.println("x = " + x);

        x/=2;
        System.out.println("x = " + x);

        x%=2;
        System.out.println("x = " + x);

    }
}
